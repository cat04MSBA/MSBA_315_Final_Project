# No-leakage test
# ---------------------------------------------------------------------------


def test_no_leakage_train_inference_unchanged_when_test_added_at_fit(
    synthetic_corpus,
):
    """Confirm the train→inference pipeline is leak-free.

    Fit on a held-out subset (films 0-7) and transform film 0 with
    that fitted model. Then fit a second model on the same first 8
    films **only** and transform film 0 again. The two transform
    outputs must be identical (the second fit is the same as the
    first; this asserts the fit step is deterministic and the
    transform output depends only on the fitted model, not on what
    other documents are in the inference batch).

    This is the correct framing of "no leakage": the cal/test films
    are not in the fit corpus, and the transform on a train film is
    independent of which other films are in the transform batch.
    """
    cfg = TopicFeatureConfig(
        n_topics=3, min_df=2, max_df=0.9, n_lda_iterations=20,
        random_state=42, remove_stopwords=False,
        stopword_set=frozenset(),
        min_token_length=3,
    )
    train_ids = list(synthetic_corpus.keys())[:8]

    # Determinism: re-fitting on the same training data produces the
    # same posterior for the same input.
    fit_a = fit_topic_model(synthetic_corpus, train_ids, cfg)
    fit_b = fit_topic_model(synthetic_corpus, train_ids, cfg)
    one_film = {train_ids[0]: synthetic_corpus[train_ids[0]]}
    df_a = compute_topic_features(one_film, fit_a)
    df_b = compute_topic_features(one_film, fit_b)
    np.testing.assert_allclose(df_a.values, df_b.values, atol=1e-10)

    # Independence of the transform batch: transforming a train film
    # alone gives the same per-film proportions as transforming it
    # alongside non-train films.
    held_out_film_id = list(synthetic_corpus.keys())[8]
    one_plus_one = {
        train_ids[0]: synthetic_corpus[train_ids[0]],
        held_out_film_id: synthetic_corpus[held_out_film_id],
    }
    df_solo = compute_topic_features(one_film, fit_a)
    df_with_extra = compute_topic_features(one_plus_one, fit_a)
    np.testing.assert_allclose(
        df_solo.loc[train_ids[0]].values,
        df_with_extra.loc[train_ids[0]].values,
        atol=1e-10,
    )


