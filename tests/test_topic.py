def test_smoke_compute_topic_features_on_first_films():
    """Smoke test on real corpus data: fit on first 100 films, transform on first 10.

    Asserts: output shape is (10, 22), no all-NaN columns, no infs,
    proportions sum to 1.0.
    """
    pkl_path = paths.DATA_PROCESSED_DIR / "screenplays_parsed.pkl"
    if not pkl_path.is_file():
        pytest.skip(f"Corpus pickle missing at {pkl_path}; run Phase 2 first")
    with pkl_path.open("rb") as f:
        full_corpus = pickle.load(f)
    ids = list(full_corpus.keys())
    train_ids = ids[:100]
    test_ids = ids[:10]

    cfg = TopicFeatureConfig(n_topics=20, n_lda_iterations=10)
    fitted = fit_topic_model(full_corpus, train_ids, cfg)
    sub = {i: full_corpus[i] for i in test_ids}
    df = compute_topic_features(sub, fitted)

    assert df.shape == (10, len(topic_feature_columns(20)))
    assert df.index.name == "imdb_id"
    assert list(df.index) == test_ids
    assert df.isna().all(axis=0).sum() == 0
    assert not np.isinf(df.values).any()

    proportion_cols = [c for c in df.columns if c.endswith("_proportion")]
    sums = df[proportion_cols].sum(axis=1)
    np.testing.assert_allclose(sums, 1.0, atol=1e-6)
