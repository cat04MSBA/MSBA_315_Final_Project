def _make_screenplay(
    imdb_id: str, dialogue: list[str],
) -> ParsedScreenplay:
    """Build a minimal one-scene ParsedScreenplay with the given dialogue lines."""
    units = tuple((f"CHAR{i}", line) for i, line in enumerate(dialogue))
    scene = Scene(
        scene_number=1,
        stage_direction="",
        scene_description="",
        dialogue_units=units,
    )
    return ParsedScreenplay(
        imdb_id=imdb_id,
        scenes=(scene,),
        n_scenes=1,
        n_unique_characters=len(units),
        n_dialogue_lines=len(units),
        total_dialogue_chars=sum(len(t) for t in dialogue),
        total_stage_direction_chars=0,
        total_scene_description_chars=0,
        total_action_chars=0,
        dialogue_to_action_ratio=1.0,
        dialogue_to_total_text_ratio=1.0,
        parse_warnings=tuple(),
    )


def _topic_corpus() -> dict[str, ParsedScreenplay]:
    """Build a 30-film synthetic corpus with three latent topics.

    Each topic is a small vocabulary; films are generated by drawing
    100 lines, each from one topic's vocabulary. This is enough for
    LDA to recover three clean topics with the configured K=3.
    """
    topics = {
        "love": ["love", "heart", "wedding", "kiss", "romance", "couple",
                 "marriage", "happy", "bride", "groom", "engaged", "rose"],
        "war": ["soldier", "battle", "rifle", "enemy", "trench", "general",
                "command", "attack", "wounded", "platoon", "ammo", "officer"],
        "space": ["spaceship", "planet", "alien", "galaxy", "stars", "rocket",
                  "captain", "warp", "asteroid", "engine", "orbit", "crew"],
    }
    rng = np.random.default_rng(42)
    corpus: dict[str, ParsedScreenplay] = {}
    for film_idx in range(30):
        topic_name = list(topics.keys())[film_idx % 3]
        words = topics[topic_name]
        lines = []
        # 100 lines per film, 8 words per line, all drawn from the
        # film's chosen topic vocabulary. With 30 films across three
        # latent topics LDA reliably separates them at K=3.
        for _ in range(100):
            line = " ".join(rng.choice(words, size=8))
            lines.append(line)
        corpus[f"tt_synth_{film_idx:02d}"] = _make_screenplay(
            f"tt_synth_{film_idx:02d}", lines,
        )
    return corpus


