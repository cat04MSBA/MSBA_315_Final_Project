@pytest.fixture(scope="module")
def synthetic_corpus():
    return _topic_corpus()


@pytest.fixture(scope="module")
def fitted_synthetic_model(synthetic_corpus):
    cfg = TopicFeatureConfig(
        n_topics=3, min_df=2, max_df=0.9, n_lda_iterations=20,
        random_state=42, remove_stopwords=False,
        # The synthetic corpus uses controlled vocabulary; remove the
        # stopword filter so the test does not depend on NLTK
        # resources.
        stopword_set=frozenset(),
        min_token_length=3,
    )
    train_ids = list(synthetic_corpus.keys())
    return fit_topic_model(synthetic_corpus, train_ids, cfg)


def test_topic_proportions_sum_to_one(fitted_synthetic_model, synthetic_corpus):
    """Per-film topic proportions sum to 1.0 within numerical tolerance."""
    df = compute_topic_features(synthetic_corpus, fitted_synthetic_model)
    proportion_cols = [c for c in df.columns if c.endswith("_proportion")]
    sums = df[proportion_cols].sum(axis=1)
    np.testing.assert_allclose(sums, 1.0, atol=1e-6)


def test_topic_dominant_id_matches_argmax(fitted_synthetic_model, synthetic_corpus):
    """``topic_dominant_id`` matches argmax of the per-film proportions."""
    df = compute_topic_features(synthetic_corpus, fitted_synthetic_model)
    proportion_cols = [c for c in df.columns if c.endswith("_proportion")]
    expected = df[proportion_cols].values.argmax(axis=1)
    np.testing.assert_array_equal(df["topic_dominant_id"].values, expected)


def test_topic_entropy_in_range(fitted_synthetic_model, synthetic_corpus):
    """``topic_concentration_entropy`` is in ``[0, 1]``."""
    df = compute_topic_features(synthetic_corpus, fitted_synthetic_model)
    h = df["topic_concentration_entropy"].values
    assert (h >= -1e-10).all()
    assert (h <= 1.0 + 1e-10).all()


def test_topic_recovery_clusters_within_group_consistently(
    fitted_synthetic_model, synthetic_corpus,
):
    """Films from the same vocabulary cluster get the same dominant topic id.

    The synthetic corpus is constructed so every third film shares a
    vocabulary. After fitting K=3 topics, films within a group land
    on the same dominant topic id (the within-group consistency
    check). Cross-group separation is not asserted because LDA's
    variational inference on small synthetic corpora can converge to
    a posterior where two well-separated clusters collapse onto a
    single topic id; this is a known property of the algorithm and
    does not reflect on the implementation.
    """
    df = compute_topic_features(synthetic_corpus, fitted_synthetic_model)
    by_group: dict[int, set[int]] = {0: set(), 1: set(), 2: set()}
    for imdb_id in df.index:
        group = int(imdb_id.split("_")[-1]) % 3
        by_group[group].add(int(df.loc[imdb_id, "topic_dominant_id"]))
    for group, topic_ids in by_group.items():
        assert len(topic_ids) == 1, (
            f"Group {group} films span multiple dominant topics: {topic_ids}"
        )


def test_topic_label_table_shape(fitted_synthetic_model):
    """``topic_label_table`` returns one row per topic with top_words populated."""
    table = topic_label_table(fitted_synthetic_model, top_n_words=5)
    assert len(table) == 3
    assert "top_words" in table.columns
    for _, row in table.iterrows():
        words = row["top_words"].split(", ")
        assert len(words) == 5


